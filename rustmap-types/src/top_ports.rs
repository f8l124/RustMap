/// Return the top N TCP ports, sorted by port number for scanning.
pub fn top_tcp_ports(n: usize) -> Vec<u16> {
    let n = n.min(TOP_TCP_PORTS.len());
    let mut ports: Vec<u16> = TOP_TCP_PORTS[..n].to_vec();
    ports.sort_unstable();
    ports.dedup();
    ports
}

/// Return the top N UDP ports, sorted by port number for scanning.
pub fn top_udp_ports(n: usize) -> Vec<u16> {
    let n = n.min(TOP_UDP_PORTS.len());
    let mut ports: Vec<u16> = TOP_UDP_PORTS[..n].to_vec();
    ports.sort_unstable();
    ports.dedup();
    ports
}

/// Default number of top ports when no `-p` is specified (matches nmap).
pub const DEFAULT_TOP_PORTS: usize = 1000;

/// Number of top ports for fast mode (`-F`).
pub const FAST_MODE_TOP_PORTS: usize = 100;

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn top_tcp_100_returns_correct_count() {
        let ports = top_tcp_ports(100);
        assert_eq!(ports.len(), 100);
    }

    #[test]
    fn top_tcp_ports_are_sorted() {
        let ports = top_tcp_ports(100);
        for w in ports.windows(2) {
            assert!(w[0] < w[1], "not sorted: {} >= {}", w[0], w[1]);
        }
    }

    #[test]
    fn top_tcp_ports_no_duplicates() {
        let ports = top_tcp_ports(TOP_TCP_PORTS.len());
        let mut deduped = ports.clone();
        deduped.dedup();
        assert_eq!(
            ports.len(),
            deduped.len(),
            "top TCP ports list has duplicates"
        );
    }

    #[test]
    fn top_udp_ports_no_duplicates() {
        let ports = top_udp_ports(TOP_UDP_PORTS.len());
        let mut deduped = ports.clone();
        deduped.dedup();
        assert_eq!(
            ports.len(),
            deduped.len(),
            "top UDP ports list has duplicates"
        );
    }

    #[test]
    fn top_tcp_contains_common_ports() {
        let ports = top_tcp_ports(100);
        assert!(ports.contains(&80), "missing port 80");
        assert!(ports.contains(&443), "missing port 443");
        assert!(ports.contains(&22), "missing port 22");
        assert!(ports.contains(&21), "missing port 21");
        assert!(ports.contains(&25), "missing port 25");
    }

    #[test]
    fn top_udp_contains_common_ports() {
        let ports = top_udp_ports(20);
        assert!(ports.contains(&53), "missing port 53");
        assert!(ports.contains(&123), "missing port 123");
        assert!(ports.contains(&161), "missing port 161");
    }

    #[test]
    fn clamped_to_max_length() {
        let ports = top_tcp_ports(999_999);
        assert_eq!(ports.len(), top_tcp_ports(TOP_TCP_PORTS.len()).len());
    }

    #[test]
    fn top_zero_returns_empty() {
        assert!(top_tcp_ports(0).is_empty());
        assert!(top_udp_ports(0).is_empty());
    }

    #[test]
    fn all_ports_in_valid_range() {
        for &p in TOP_TCP_PORTS {
            assert!(p >= 1, "invalid TCP port: {p}");
        }
        for &p in TOP_UDP_PORTS {
            assert!(p >= 1, "invalid UDP port: {p}");
        }
    }

    #[test]
    fn constants_correct() {
        assert_eq!(DEFAULT_TOP_PORTS, 1000);
        assert_eq!(FAST_MODE_TOP_PORTS, 100);
    }
}
